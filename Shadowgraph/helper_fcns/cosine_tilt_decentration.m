function F = cosine_tilt_decentration(a, data)
r = size(data,1)/2;
x = data(1:r, :);
y = data(r+1:2*r, :);



F = a(1,1) + ...
    (a(2,1)).*cos(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1)) + ...
    (a(3,1)).*cos(2*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(4,1)).*cos(3*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(5,1)).*cos(4*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(6,1)).*cos(5*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(7,1)).*cos(6*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(8,1)).*cos(7*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(9,1)).*cos(8*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(10,1)).*cos(9*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1))) + ...
    (a(11,1)).*cos(10*(atan2((y-a(13,1)),(x-a(12,1)))-a(14,1)));





